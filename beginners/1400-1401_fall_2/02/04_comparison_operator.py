# عملگرهای مقایسه ای

# boolean - [True, False]

a = 12
b = 13
c = 12

print(f"{a} > {b} = {a > b}")
print(f"{a} >= {c} = {a >= c}")
print(f"{a} < {b} = {a < b}")
print(f"{a} <= {c} = {a <= c}")

# چک میکند آیا مقدارهایش مساوی هست یا نه
print(f"{a} == {c} = {a == c}")  # equal
print(f"{a} != {c} = {a != c}")  # not equal
print(f"{a} != {b} = {a != b}")  # not equal
