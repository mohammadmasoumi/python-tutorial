# datatype

"""
int
float
str
bool
"""

a = 12  # int
print(a, type(a))

b = 12.5  # float
print(b, type(b))

c = "this is a string"  # str
d = 'this is a string'  # str
print(c, type(c))
print(d, type(d))

# True, False
e = True  # bool
f = False  # bool

print(e, type(e))
print(f, type(f))
