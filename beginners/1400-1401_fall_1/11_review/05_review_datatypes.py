# 12 is value of a
a = 12 
print(f"a: {type(a)}")  # a: <class 'int'>

#
b = 12.2
print(f"b: {type(b)}")  # b: <class 'float'>

#
c = "Hello"
print(f"c: {type(c)}")  # c: <class 'str'>

# True, False - data-types
d = True
print(f"d: {type(d)}")  # d: <class 'bool'>

# 
e = [1, 2, 3]
print(f"e: {type(e)}")  # e: <class 'bool'>
