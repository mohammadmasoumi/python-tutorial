# update

d1 = {
    'k1': 'v11',
    'k2': 'v12',
    'k3': 'v13',
    'k4': 'v14',
}

d2 = {
    'k2': 'v22',
    'k3': 'v23',
}

# *[1, 2, 3] =>(remove brackets) 1, 2, 3

# unpacking
# **{'k2': 'v22','k3': 'v23'} =>(remove curly braces) 'k2': 'v22','k3': 'v23'
d1 = {**d1, **d2}

"""
{
    # **{
    'k1': 'v11',
    'k2': 'v12',
    'k3': 'v13',
    'k4': 'v14',
    # }
    # **{
    'k2': 'v22',
    'k3': 'v23',
    # }
}
d1.update(d2)
"""

d3 = {**d1, **d2}


