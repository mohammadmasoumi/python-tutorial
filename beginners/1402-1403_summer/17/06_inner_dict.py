# inner dictionary
d = {
    'a': {
        'b': {
            'c': [3, {
                'd': ({'e': 1}, {'g': 2})
            }]
        }
    }
}

print(d['a']) # {'b': {'c': [3, {'d': ({'e': 1}, {'g': 2})}]}}
print(d['a']['b']) # {'c': [3, {'d': ({'e': 1}, {'g': 2})}]}
print(d['a']['b']['c'])  # [3, {'d': ({'e': 1}, {'g': 2})}]
print(d['a']['b']['c'][1]) # {'d': ({'e': 1}, {'g': 2})}
print(d['a']['b']['c'][1]['d']) # ({'e': 1}, {'g': 2})
print(d['a']['b']['c'][1]['d'][1]) # {'g': 2}
print(d['a']['b']['c'][1]['d'][1]['g'])

print(d.get('a', {})) # {'b': {'c': [3, {'d': ({'e': 1}, {'g': 2})}]}}
print(d.get('a', {})['b']) # {'c': [3, {'d': ({'e': 1}, {'g': 2})}]}
print(d.get('a', {})['b']['c'])  # [3, {'d': ({'e': 1}, {'g': 2})}]
print(d.get('a', {})['b']['c'][1]) # {'d': ({'e': 1}, {'g': 2})}
print(d.get('a', {})['b']['c'][1]['d']) # ({'e': 1}, {'g': 2})
print(d.get('a', {})['b']['c'][1]['d'][1]) # {'g': 2}
print(d.get('a', {})['b']['c'][1]['d'][1]['g'])

