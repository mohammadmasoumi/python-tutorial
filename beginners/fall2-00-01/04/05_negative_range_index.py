#             0         1        2         3          4
#            -5         -4       -3        -2         -1
fruits = ["orange", "banana", "apple", "cucumber", "peach"]

# range index

"""
list_name[start_index(included): end_index(not included): 1]
list_name[start_index(included): end_index(not included): step]
"""

# sub list
print(fruits[-1])

# صعودی
print(fruits[-1: -2])

#
# start from beginning
# [:end]

# go to the end
# [start:]

print(fruits[-3: -1])
print(fruits[-3: 1])  # empty
print(fruits[-3:])
print(fruits[:4])
